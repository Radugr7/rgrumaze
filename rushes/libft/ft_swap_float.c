#include "libft.h"

void	ft_swap_float(float *p1, float *p2)
{
	float		*aux;

	aux = (float*)malloc(sizeof(float));
	*aux = *p2;
	*p2 = *p1;
	*p1 = *aux;
	free(aux);
}
